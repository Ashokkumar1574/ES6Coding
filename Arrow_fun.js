// Basic arrow function
const add = (a, b) => a + b;

// Arrow function with one parameter (no parentheses needed)
const square = x => x * x;

// Arrow function with no parameters
const greet = () => "Hello!";

// Arrow function with multiple statements (needs curly braces and return)
const multiply = (a, b) => {
  const result = a * b;
  return result;
};